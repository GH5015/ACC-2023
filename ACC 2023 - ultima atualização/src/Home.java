    public List<Object[]> getAllSuppliersFromDB() {
    List<Object[]> suppliers = new ArrayList<>();
    String query = "SELECT nome, produtoFornecido, telefone FROM fornecedores";
    try (Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
         PreparedStatement statement = connection.prepareStatement(query);
         ResultSet resultSet = statement.executeQuery()) {

        while (resultSet.next()) {
            suppliers.add(new Object[] {
                resultSet.getString("nome"),
                resultSet.getString("produtoFornecido"),
                resultSet.getString("telefone")
            });
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    return suppliers;
}
    
