    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if(
            descField.getText()!=null &&
            !descField.getText().isEmpty() &&
            dateField.getText()!=null &&
            !dateField.getText().isEmpty()){
        String description = descField.getText();
        String date = dateField.getText();
        
        try {
                    Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
                    PreparedStatement statement = connection.prepareStatement("INSERT INTO metas (descrição, prazo) VALUES (?, ?)");

                    statement.setString(1, description);
                    statement.setString(2, date);

                    int rowsInserted = statement.executeUpdate();
                    
                    JOptionPane.showMessageDialog(addButton, "Meta cadastrada!");
                    
                    DefaultTableModel modelT = (DefaultTableModel) tableTask.getModel();
                    modelT.setRowCount(0);
                    List<Object[]> tasks = getAllTasksFromDB();
                    
                    for (Object[] task : tasks) {
                    modelT.addRow(task);
                }
                    
                    descField.setText("");
                    dateField.setText("");

                    // Close the resources
                    statement.close();
                    connection.close();

                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(addButton, "Erro conectando com a database.", "Aviso", JOptionPane.WARNING_MESSAGE);
                }
        
        }
        else{
            JOptionPane.showMessageDialog(addButton, "Verifique as informações", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

